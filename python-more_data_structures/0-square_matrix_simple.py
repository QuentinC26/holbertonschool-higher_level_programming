def square_matrix_simple(matrix=[]):

    new_matrix = []

    for row in new_matrix:
        # row = ligne horizontal
        for index in range(len(row)):
            if matrix[index][row(index)] * matrix[index][row(index)]:
                new_matrix.append(True)
            else:
                new_matrix.append(False)
        if len(row) == 0:   
            print("")
    return new_matrix
